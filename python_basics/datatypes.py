#int
myint = 10
print(myint)
print(type(myint))

#float
myfloat=0.7
print(myfloat)
print(type(myfloat))

#complex
mycomplex=complex('3+2j')
print(mycomplex)
print(type(mycomplex))

#list
mylist=[1,2,3]
print(mylist)
print(type(mylist))

#tuple
mytuple=(1,2,3)
print(mytuple)
print(type(mytuple))

#dict
mydict={
    "Kenya":"nairobi",
    "uganda":"kampala",
    "somalia":"mogadishu",
}
